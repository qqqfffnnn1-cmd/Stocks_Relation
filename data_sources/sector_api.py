def get_sector_stocks(sector_code: str):
    """返回板块成分股，优先用硬编码，fallback到API"""
    if sector_code in SECTOR_STOCKS:
        return SECTOR_STOCKS[sector_code]

    # fallback: 尝试东方财富API
    try:
        import requests
        s = requests.Session()
        s.trust_env = False
        s.headers.update({"User-Agent": "Mozilla/5.0"})
        url = "https://push2.eastmoney.com/api/qt/clist/get"
        params = {
            "pn": 1, "pz": 30, "po": 1, "np": 1,
            "ut": "bd1d9ddb04089700cf9c27f6f7426281",
            "fltt": 2, "invt": 2, "fid": "f3",
            "fs": f"b:{sector_code} f:!50",
            "fields": "f12,f14",
        }
        resp = s.get(url, params=params, timeout=8)
        items = resp.json()["data"]["diff"]
        return [x["f12"] for x in items]
    except Exception as e:
        print(f"获取板块成分股失败: {e}")
        return []
